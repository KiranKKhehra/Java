package jul22.week1.day1.assignments;

public class FibonacciSeries {

	public static void main(String[] args) {
		
	int Num= 8;
		int firstNum=0;
		int secNum=1;
		int sum=1;
		
		System.out.println("firstNum");
	
		for (int i= 1;i<=Num; i++);
		
		{
		sum=firstNum+secNum;
		firstNum=secNum;
		secNum= sum;
		System.out.println("sum");
		}
		
		
		

	}

}
